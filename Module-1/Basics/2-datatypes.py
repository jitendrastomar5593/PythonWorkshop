## Numeric Type:

# 1. int - represens integer number.
myAge = 30
print (type(myAge))

# 2. float - represents floating number.
PI = 3.14
print (type(PI))

# 3. complex - represents complex numbers.
z = 2 + 3j
print (type(z))

## Boolean Type:

# 4. bool - Represents boolean values.
is_active  = True
is_stopped = False
print (type(is_active))
print (type(is_stopped))

## Sequence Type

# 5. str - represents a string of characters.
tech = "Python"
print (type(tech))

#6. list - Represents an ordered collection of items
coll_item = [1,2,3,4,5]
print (type(coll_item))

## Mapping Type

# 7. dict - Represents a collection of key-value pairs.
employee = {'name': 'Jitendra', 'designation': 'Trainer'}
print (type(employee))

## Set Type

# 8. set - Represents an unordered collection of unique elements.
unique_numbers = {1, 2, 3, 4, 5}
print (type(unique_numbers))

# 9. frozenset - Represents an immutable set.
vowels = frozenset({'a', 'e', 'i', 'o', 'u'})
print (type(vowels))

## None Type
# None - Represents the absence of a value or a null value
result = None
print (type(result))
